package com.xsis.Day04;

public class latihanmatrix3 {
    public static void main(String[] args) {
        int[][] matrix = new int [7] [7];
        for (int i=0;i<matrix.length;i++){
            for (int j=0;j<matrix.length;j++){
                System.out.print(i);
                System.out.print(j+ " ");
            }
            System.out.println();
        }
    }
}


